### Resumo da Aula sobre Sustentabilidade e Práticas Empresariais

#- **Sustentabilidade**: Envolve o equilíbrio entre as dimensões **econômica**, **ambiental** e **social**, pautando-se nos princípios do ESG e no conceito de **Triple Bottom Line**. O desenvolvimento sustentável busca atender às necessidades atuais sem comprometer as gerações futuras.

#- **Objetivos Empresariais**: As empresas adotam práticas sustentáveis por motivos legais (exigências do Ibama, Lei de Cotas) ou para gerar valor (Responsabilidade Social Empresarial, RSE). A **ISO 26000** fornece diretrizes sobre RSE, ressaltando a importância de atuar de maneira ética e transparente.

#- **Diretrizes da ISO 26000**:
#  1. Accountability: Responsabilidade pela transparência das ações.
#  2. Transparência: Clareza em atividades com impacto.
#  3. Comportamento Ético: Decisões pautadas em valores éticos.
#  4. Respeito pelos Stakeholders: Consideração dos interesses de todos.
#  5. Estado de Direito: Respeito às leis.
#  6. Normas Internacionais: Respeito às normas globais.
#  7. Direitos Humanos: Promoção dos direitos humanos.

#- **TI Verde**: Práticas de tecnologia da informação que minimizam o impacto ambiental. As categorias incluem:
#  1. TI Verde Tática: Melhoria de processos com infraestruturas existentes.
#  2. TI Verde Estratégica: Integração de TI e sustentabilidade.
#  3. TI Verde Profunda: Mudanças estruturais para eficiência.

#- **Economia Circular**: Envolve a eliminação de resíduos e reintrodução de materiais no ciclo produtivo, promovendo um design circular e produção limpa.

#- **Certificações e Iniciativas**: Certificações como **EPEAT** e **Energy Star** garantem produtos sustentáveis, enquanto legislações como a **Política Nacional de Resíduos Sólidos** regulam a produção e descarte.

#A implementação de práticas sustentáveis é essencial para minimizar impactos ambientais e avançar rumo a um futuro ético e sustentável.